
public class QuotientAndReminder {

	public static void main(String[] args) {
	
		 int dividend = 30;
		 int divisor = 4;
		 
		 int quotient = dividend / divisor;   //quotient = when you dived 2 numbers to each other the result is quotient
		 
		 int reminder = dividend % divisor; // reminder = after division what ever left over is reminder
		 
		 System.out.println(quotient);
		 System.out.println(reminder);
	}

}
